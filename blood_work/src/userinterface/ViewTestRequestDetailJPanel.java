    private void populateForm() {
        txtSampleCollectionDate.setText(tr.getCollectionDate() == null ? "" : tr.getCollectionDate().toString());
        txtStatus.setText(tr.getStatus());
        if (tr.getNucleicAcidTestDate() == null) {
            txtResult.setText("");
        }else {
            txtResult.setText(tr.isPositive() == true ? "Positive" : "Negative");
        }
        txtSlotReleaseDate.setText(tr.getTestSlotRequest().getSlotReleaseDate().toString());
        txtScheduledTestingDate.setText(tr.getTestSlotRequest().getScheduledTestingDate().toString());
    }

